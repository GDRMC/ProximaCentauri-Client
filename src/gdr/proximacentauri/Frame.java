    public void reach() throws FileNotFoundException {
        int search = db.search(jTextField1.getText());
        if(!(search==-1)){
            String user = db.getUsername(search);
            String hash = db.getUserHash(search);
            if(jPasswordField1.getPassword().toString().equals(hash)){
                pop.showInformationMessage(1, true);
                launchApp();
            }
        } 
        else {
            pop.showErrorMessage(-1,true);
        }
    }
    
